  "id" bigint NOT NULL GENERATED BY DEFAULT AS IDENTITY,
  "provider" character varying NOT NULL,
  "bronze_table" character varying NOT NULL,
  "bronze_resource_id" character varying NOT NULL,
  "silver_installed_software_bronze_links" character varying NOT NULL,
  PRIMARY KEY ("id"),
  CONSTRAINT "installed_software_bronze_links_installed_software_bronze_links" FOREIGN KEY ("silver_installed_software_bronze_links") REFERENCES "silver"."installed_software" ("resource_id") ON UPDATE NO ACTION ON DELETE NO ACTION
