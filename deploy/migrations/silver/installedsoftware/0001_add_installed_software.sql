-- Create index "silverinstalledsoftware_name" to table: "installed_software"
CREATE INDEX "silverinstalledsoftware_name" ON "silver"."installed_software" ("name");
-- Create "installed_software_normalized" table
CREATE TABLE "silver"."installed_software_normalized" (
  "resource_id" character varying NOT NULL,
  "provider" character varying NOT NULL,
  "is_base" boolean NOT NULL,
  "bronze_table" character varying NOT NULL,
  "bronze_resource_id" character varying NOT NULL,
  "machine_id" character varying NOT NULL,
  "name" character varying NOT NULL,
  "version" character varying NULL,
  "publisher" character varying NULL,
  "collected_at" timestamptz NOT NULL,
  "first_collected_at" timestamptz NOT NULL,
  "normalized_at" timestamptz NOT NULL,
  PRIMARY KEY ("resource_id")
