-- Add new schema named "inventory"
CREATE SCHEMA IF NOT EXISTS "inventory";
-- Create "software" table
CREATE TABLE "inventory"."software" (
  "resource_id" character varying NOT NULL,
  "collected_at" timestamptz NOT NULL,
  "first_collected_at" timestamptz NOT NULL,
  "normalized_at" timestamptz NOT NULL,
  "machine_id" character varying NOT NULL,
  "name" character varying NOT NULL,
  "version" character varying NULL,
  "publisher" character varying NULL,
  PRIMARY KEY ("resource_id")
);
-- Create index "inventorysoftware_machine_id" to table: "software"
CREATE INDEX "inventorysoftware_machine_id" ON "inventory"."software" ("machine_id");
-- Create index "inventorysoftware_machine_id_name" to table: "software"
CREATE UNIQUE INDEX "inventorysoftware_machine_id_name" ON "inventory"."software" ("machine_id", "name");
-- Create index "inventorysoftware_name" to table: "software"
CREATE INDEX "inventorysoftware_name" ON "inventory"."software" ("name");
-- Create "software_normalized" table
CREATE TABLE "inventory"."software_normalized" (
  "resource_id" character varying NOT NULL,
  "provider" character varying NOT NULL,
  "is_base" boolean NOT NULL,
  "bronze_table" character varying NOT NULL,
  "bronze_resource_id" character varying NOT NULL,
  "machine_id" character varying NOT NULL,
  "name" character varying NOT NULL,
  "version" character varying NULL,
  "publisher" character varying NULL,
  "collected_at" timestamptz NOT NULL,
  "first_collected_at" timestamptz NOT NULL,
  "normalized_at" timestamptz NOT NULL,
  PRIMARY KEY ("resource_id")
);
-- Create index "inventorysoftwarenormalized_machine_id_name" to table: "software_normalized"
CREATE INDEX "inventorysoftwarenormalized_machine_id_name" ON "inventory"."software_normalized" ("machine_id", "name");
-- Create index "inventorysoftwarenormalized_provider" to table: "software_normalized"
CREATE INDEX "inventorysoftwarenormalized_provider" ON "inventory"."software_normalized" ("provider");
-- Create index "inventorysoftwarenormalized_provider_bronze_resource_id" to table: "software_normalized"
CREATE UNIQUE INDEX "inventorysoftwarenormalized_provider_bronze_resource_id" ON "inventory"."software_normalized" ("provider", "bronze_resource_id");
-- Create "software_bronze_links" table
CREATE TABLE "inventory"."software_bronze_links" (
  "id" bigint NOT NULL GENERATED BY DEFAULT AS IDENTITY,
  "provider" character varying NOT NULL,
  "bronze_table" character varying NOT NULL,
  "bronze_resource_id" character varying NOT NULL,
  "inventory_software_bronze_links" character varying NOT NULL,
  PRIMARY KEY ("id"),
  CONSTRAINT "software_bronze_links_software_bronze_links" FOREIGN KEY ("inventory_software_bronze_links") REFERENCES "inventory"."software" ("resource_id") ON UPDATE NO ACTION ON DELETE NO ACTION
);
