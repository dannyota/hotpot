-- Add new schema named "inventory"
CREATE SCHEMA IF NOT EXISTS "inventory";
-- Create "machine_normalized" table
CREATE TABLE "inventory"."machine_normalized" (
  "resource_id" character varying NOT NULL,
  "provider" character varying NOT NULL,
  "is_base" boolean NOT NULL,
  "bronze_table" character varying NOT NULL,
  "bronze_resource_id" character varying NOT NULL,
  "hostname" character varying NULL,
  "os_type" character varying NULL,
  "os_name" character varying NULL,
  "status" character varying NOT NULL,
  "internal_ip" character varying NULL,
  "external_ip" character varying NULL,
  "environment" character varying NULL,
  "cloud_project" character varying NULL,
  "cloud_zone" character varying NULL,
  "cloud_machine_type" character varying NULL,
  "merge_keys_json" jsonb NOT NULL,
  "collected_at" timestamptz NOT NULL,
  "first_collected_at" timestamptz NOT NULL,
  "normalized_at" timestamptz NOT NULL,
  PRIMARY KEY ("resource_id")
);
-- Create index "inventorymachinenormalized_provider" to table: "machine_normalized"
CREATE INDEX "inventorymachinenormalized_provider" ON "inventory"."machine_normalized" ("provider");
-- Create index "inventorymachinenormalized_provider_bronze_resource_id" to table: "machine_normalized"
CREATE UNIQUE INDEX "inventorymachinenormalized_provider_bronze_resource_id" ON "inventory"."machine_normalized" ("provider", "bronze_resource_id");
-- Create "machines" table
CREATE TABLE "inventory"."machines" (
  "resource_id" character varying NOT NULL,
  "collected_at" timestamptz NOT NULL,
  "first_collected_at" timestamptz NOT NULL,
  "normalized_at" timestamptz NOT NULL,
  "hostname" character varying NOT NULL,
  "os_type" character varying NOT NULL,
  "os_name" character varying NULL,
  "status" character varying NOT NULL,
  "internal_ip" character varying NULL,
  "external_ip" character varying NULL,
  "environment" character varying NULL,
  "cloud_project" character varying NULL,
  "cloud_zone" character varying NULL,
  "cloud_machine_type" character varying NULL,
  PRIMARY KEY ("resource_id")
);
-- Create index "inventorymachine_collected_at" to table: "machines"
CREATE INDEX "inventorymachine_collected_at" ON "inventory"."machines" ("collected_at");
-- Create index "inventorymachine_environment" to table: "machines"
CREATE INDEX "inventorymachine_environment" ON "inventory"."machines" ("environment");
-- Create index "inventorymachine_os_type" to table: "machines"
CREATE INDEX "inventorymachine_os_type" ON "inventory"."machines" ("os_type");
-- Create "machine_bronze_links" table
CREATE TABLE "inventory"."machine_bronze_links" (
  "id" bigint NOT NULL GENERATED BY DEFAULT AS IDENTITY,
  "provider" character varying NOT NULL,
  "bronze_table" character varying NOT NULL,
  "bronze_resource_id" character varying NOT NULL,
  "inventory_machine_bronze_links" character varying NOT NULL,
  PRIMARY KEY ("id"),
  CONSTRAINT "machine_bronze_links_machines_bronze_links" FOREIGN KEY ("inventory_machine_bronze_links") REFERENCES "inventory"."machines" ("resource_id") ON UPDATE NO ACTION ON DELETE NO ACTION
);
