-- Add new schema named "bronze"
CREATE SCHEMA IF NOT EXISTS "bronze";
-- Create "jenkins_jobs" table
CREATE TABLE "bronze"."jenkins_jobs" (
  "resource_id" character varying NOT NULL,
  "collected_at" timestamptz NOT NULL,
  "first_collected_at" timestamptz NOT NULL,
  "job_class" character varying NULL,
  "project_type" character varying NULL,
  "is_buildable" boolean NOT NULL DEFAULT false,
  "last_build_number" bigint NOT NULL DEFAULT 0,
  "last_build_time" timestamptz NULL,
  PRIMARY KEY ("resource_id")
);
-- Create index "bronzejenkinsjob_collected_at" to table: "jenkins_jobs"
CREATE INDEX "bronzejenkinsjob_collected_at" ON "bronze"."jenkins_jobs" ("collected_at");
-- Create index "bronzejenkinsjob_is_buildable" to table: "jenkins_jobs"
CREATE INDEX "bronzejenkinsjob_is_buildable" ON "bronze"."jenkins_jobs" ("is_buildable");
-- Create "jenkins_builds" table
CREATE TABLE "bronze"."jenkins_builds" (
  "id" bigint NOT NULL GENERATED BY DEFAULT AS IDENTITY,
  "build_number" bigint NOT NULL,
  "result" character varying NULL,
  "timestamp" timestamptz NULL,
  "duration_ms" bigint NOT NULL DEFAULT 0,
  "version" character varying NULL,
  "check_code_enabled" boolean NOT NULL DEFAULT false,
  "bronze_jenkins_job_builds" character varying NOT NULL,
  PRIMARY KEY ("id"),
  CONSTRAINT "jenkins_builds_jenkins_jobs_builds" FOREIGN KEY ("bronze_jenkins_job_builds") REFERENCES "bronze"."jenkins_jobs" ("resource_id") ON UPDATE NO ACTION ON DELETE NO ACTION
);
-- Create index "bronzejenkinsbuild_build_number" to table: "jenkins_builds"
CREATE INDEX "bronzejenkinsbuild_build_number" ON "bronze"."jenkins_builds" ("build_number");
-- Create index "bronzejenkinsbuild_result" to table: "jenkins_builds"
CREATE INDEX "bronzejenkinsbuild_result" ON "bronze"."jenkins_builds" ("result");
-- Create "jenkins_build_repos" table
CREATE TABLE "bronze"."jenkins_build_repos" (
  "id" bigint NOT NULL GENERATED BY DEFAULT AS IDENTITY,
  "repo_url" character varying NULL,
  "branch" character varying NULL,
  "commit_sha" character varying NULL,
  "bronze_jenkins_build_repos" bigint NOT NULL,
  PRIMARY KEY ("id"),
  CONSTRAINT "jenkins_build_repos_jenkins_builds_repos" FOREIGN KEY ("bronze_jenkins_build_repos") REFERENCES "bronze"."jenkins_builds" ("id") ON UPDATE NO ACTION ON DELETE NO ACTION
);
